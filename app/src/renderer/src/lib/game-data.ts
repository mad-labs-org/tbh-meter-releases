// ── Blue-chest cooldown helpers (#265) ──────────────────────────────────────
// Everything a cooldown card needs is DERIVED from a stageKey here (the engine only stores
// stageKey + dropAt), so this file stays the single source of chest/stage facts.

/** Title-cased difficulty for a stage ("NORMAL" -> "Normal"), matching the mode-badge keys
 *  in format.ts. Null when the stage (or its difficulty) is unknown. */
export function stageDifficulty(key: number): string | null {
  const d = stageMap.get(key)?.difficulty;
  if (!d) return null;
  return d.charAt(0) + d.slice(1).toLowerCase();
}

/** Compact stage code "act-stageNo" (e.g. "2-1"), the in-game shorthand. Null if unknown. */
export function stageCode(key: number): string | null {
  const s = stageMap.get(key);
  return s ? `${s.act}-${s.stageNo}` : null;
}

/** The blue-chest BASE drop rate for a stage (game per-mille `bossDrop.rate`), or null if
 *  unmapped. Base only — rune/pet "chest drop chance" bonuses aren't in any reader data. */
export function bossBoxRate(key: number): number | null {
  return stageMap.get(key)?.bossDrop?.rate ?? null;
}

/** Game `rate` (per-mille) → percent string. 8 → "0.8%", 150 → "15%", 1000 → "100%". */
export function formatDropRate(rate: number): string {
  return `${rate / 10}%`;
}

// box key -> the stageKeys whose blue chest IS that box (one box drops across several stages).
const stagesByBoxMap = new Map<number, number[]>();
for (const s of stages) {
  const box = s.bossDrop?.itemKey;
  if (box == null) continue;
  const list = stagesByBoxMap.get(box);
  if (list) list.push(s.key);
  else stagesByBoxMap.set(box, [s.key]);
}

/** Stage keys that drop the given blue-chest box (i.e. the same chest level), in data order. */
export function stagesByBox(boxKey: number): number[] {
  return stagesByBoxMap.get(boxKey) ?? [];
}

/** Chest level for a box ("Stage Boss Box Lv80" -> 80). Delegates to the shared parser so the
 *  card and the main-process notification show the same level. */
export function chestLevel(boxKey: number): number | null {
  return boxLevel(boxKey);
}

/** The highest-base-rate stage that drops this box — the best farm spot, used for the card's
 *  headline rate and the "open stage" link (a route placeholder has no origin stage of its own). */
export function boxBestStage(boxKey: number | null | undefined): number | null {
  if (boxKey == null) return null;
  let best: number | null = null;
  let bestRate = -1;
  for (const k of stagesByBox(boxKey)) {
    const r = bossBoxRate(k) ?? -1;
    if (r > bestRate) {
      bestRate = r;
      best = k;
    }
  }
  return best;
}

// --- Blue box (Stage Boss Box) cross-mode drop spots -------------------------
// For a blue box, WHERE it drops: per mode, the stage ranges grouped by BASE rate
// ("first~last"). Feeds the cooldown card's "spots" row. Rune/pet "chest chance"
// bonuses aren't in any reader data, so this is BASE rate only.

const MODE_FROM_DIFFICULTY: Record<string, string> = {
  NORMAL: "Normal",
  NIGHTMARE: "Nightmare",
  HELL: "Hell",
  TORMENT: "Torment",
};
const MODE_RANK: Record<string, number> = { Normal: 0, Nightmare: 1, Hell: 2, Torment: 3 };

/** One stage-range at one rate, e.g. { range: "3-5~3-9", pct: "15%" }. */
export interface BlueBoxSegment {
  range: string;
  rate: number;
  pct: string;
}
/** All spots for the blue box within one mode, highest-rate range first. */
export interface BlueBoxMode {
  mode: string;
  segments: BlueBoxSegment[];
}

/** Every mode/stage a blue box drops at, grouped by rate into `first~last` ranges and ordered
 *  Normal→Torment. Null when the key isn't a blue box (e.g. an act-boss box → 930xxx). Keyed by
 *  box (not stage) since a cooldown card represents the chest level, not one stage. */
export function blueBoxSpotsForBox(boxKey: number | null | undefined): BlueBoxMode[] | null {
  if (boxKey == null || boxKey < 920000 || boxKey >= 930000) return null; // blue box only

  // Reuse the prebuilt box→stages index instead of re-scanning all stages.
  const byMode = new Map<string, { act: number; stageNo: number; rate: number }[]>();
  for (const k of stagesByBox(boxKey)) {
    const s = stageMap.get(k);
    if (s?.bossDrop?.rate == null || !s.difficulty) continue;
    const mode = MODE_FROM_DIFFICULTY[s.difficulty] ?? s.difficulty;
    let arr = byMode.get(mode);
    if (!arr) byMode.set(mode, (arr = []));
    arr.push({ act: s.act, stageNo: s.stageNo, rate: s.bossDrop.rate });
  }
  if (byMode.size === 0) return null;

  const modes: BlueBoxMode[] = [];
  for (const [mode, spots] of byMode) {
    spots.sort((a, b) => a.act - b.act || a.stageNo - b.stageNo);
    // Rates fall monotonically with depth, so each distinct rate is a contiguous
    // stage range — group by rate, highest first, and collapse to first~last.
    const byRate = new Map<number, string[]>();
    for (const sp of spots) {
      let g = byRate.get(sp.rate);
      if (!g) byRate.set(sp.rate, (g = []));
      g.push(`${sp.act}-${sp.stageNo}`);
    }
    const segments: BlueBoxSegment[] = [...byRate.entries()]
      .sort((a, b) => b[0] - a[0])
      .map(([rate, sts]) => ({
        range: sts[0] === sts[sts.length - 1] ? sts[0] : `${sts[0]}~${sts[sts.length - 1]}`,
        rate,
        pct: formatDropRate(rate),
      }));
    modes.push({ mode, segments });
  }
  modes.sort((a, b) => (MODE_RANK[a.mode] ?? 9) - (MODE_RANK[b.mode] ?? 9));
  return modes;
}
