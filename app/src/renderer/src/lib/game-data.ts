interface HeroRecord {
  key: number;
  name: string;
  classType: string;
}

interface SkillRecord {
  key: number;
  name: string | null;
  names: Record<string, string> | null;
}

interface StageRecord {
  key: number;
  name: string;
  names: Record<string, string> | null;
  act: number;
  stageNo: number;
  difficulty?: string;
  bossDrop?: { itemKey: number; rate: number } | null;
  /** Weighted spawn table (monster keys are strings in the datamine output). */
  monsters?: { monster: string; weight: number }[];
  bossMonsterKey?: number | null;
}

const heroes = heroesData as HeroRecord[];
const skills = skillsData as SkillRecord[];
const stages = stagesData as StageRecord[];

export const heroMap = new Map<number, HeroRecord>(heroes.map((h) => [h.key, h]));
export const skillMap = new Map<number, SkillRecord>(skills.map((s) => [s.key, s]));
export const stageMap = new Map<number, StageRecord>(stages.map((s) => [s.key, s]));

export function heroName(key: number): string {
  return heroMap.get(key)?.name ?? `Hero ${key}`;
}

export function skillName(key: number): string {
  const s = skillMap.get(key);
  if (!s) return `Skill ${key}`;
  return s.names?.["en-us"] ?? s.name ?? `Skill ${key}`;
}

export function stageName(key: number): string {
  const s = stageMap.get(key);
  if (!s) return `Stage ${key}`;
  return s.names?.["en-us"] ?? s.name ?? `Stage ${key}`;
}

